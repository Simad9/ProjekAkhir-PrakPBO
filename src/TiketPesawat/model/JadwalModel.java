package TiketPesawat.model;

public class JadwalModel {
    private String kodeJadwal = "";
    private String kodePesawat = "";
    private String kotaAwal = "";
    private String kotaTujuan = "";
    private String jamKeberangkatan = "";
    private String jamKedatangan = "";
    private String harga = "";

    public String getKodeJadwal() {
        return kodeJadwal;
    }

    public void setKodeJadwal(String kodeJadwal) {
        this.kodeJadwal = kodeJadwal;
    }

    public String getKodePesawat() {
        return kodePesawat;
    }

    public void setKodePesawat(String kodePesawat) {
        this.kodePesawat = kodePesawat;
    }

    public String getKotaAwal() {
        return kotaAwal;
    }

    public void setKotaAwal(String kotaAwal) {
        this.kotaAwal = kotaAwal;
    }

    public String getKotaTujuan() {
        return kotaTujuan;
    }

    public void setKotaTujuan(String kotaTujuan) {
        this.kotaTujuan = kotaTujuan;
    }

    public String getJamKeberangkatan() {
        return jamKeberangkatan;
    }

    public void setJamKeberangkatan(String jamKeberangkatan) {
        this.jamKeberangkatan = jamKeberangkatan;
    }

    public String getJamKedatangan() {
        return jamKedatangan;
    }

    public void setJamKedatangan(String jamKedatangan) {
        this.jamKedatangan = jamKedatangan;
    }

    public String getHarga() {
        return harga;
    }

    public void setHarga(String harga) {
        this.harga = harga;
    }
    
    
    
}
